/*
Write a Java program to take the length and breadth of a rectangle and print its area. (Easy)
Sample Input : 7, 4
Sample Output : 28 
*/
public class area_of_rectangle {
    public static void main(String[] args){
        int lenght=7;
        int breadth=4;
        int area_of_rectangle=lenght*breadth;
        System.out.println(area_of_rectangle);
    }
}
